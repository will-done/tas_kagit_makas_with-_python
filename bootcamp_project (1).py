import random

def get_computer_choice():
    choices = ["rock", "paper", "scissors"]
    return random.choice(choices)

def determine_winner(player_choice, computer_choice):
    if player_choice == computer_choice:
        return "It's a tie!"
    elif (player_choice == "rock" and computer_choice == "scissors") or \
         (player_choice == "paper" and computer_choice == "rock") or \
         (player_choice == "scissors" and computer_choice == "paper"):
        return "Player wins!"
    else:
        return "Computer wins!"

def get_valid_player_choice():
    valid_choices = ["rock", "paper", "scissors"]
    while True:
        player_choice = input("Please choose rock, paper, scissors or exit: ").lower()
        if player_choice in valid_choices:
            return player_choice
        elif player_choice == "exit":
            print("Exiting the game. Hope you had fun! Come back for more rock-paper-scissors action!")
            return None
        else:
            print("Invalid choice. Please enter 'rock', 'paper', or 'scissors'. Try again!")

def play_round():
    player_choice = get_valid_player_choice()
    if player_choice is None:
        return None, None

    computer_choice = get_computer_choice()
    print(f"Computer chose: {computer_choice}")
    result = determine_winner(player_choice, computer_choice)
    print()
    print(result)
    print()  # Adding a blank line for better readability
    return result, player_choice

def ask_play_again():
    while True:
        response = input("Do you want to play another game? (yes/no): ").lower()
        if response in ["yes", "no"]:
            return response
        else:
            print("Invalid response. Please enter 'yes' or 'no'.")

